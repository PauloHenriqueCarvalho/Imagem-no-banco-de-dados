package model.bean;

import java.sql.Blob;

/**
 *
 * @author paulo
 */
public class Produto {
    private int id;
    private String nome;
    private float preco;
    private Blob imagem;

    public Produto() {
    }

    public Produto(int id, String nome, float preco, Blob imagem) {
        this.id = id;
        this.nome = nome;
        this.preco = preco;
        this.imagem = imagem;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public Blob getImagem() {
        return imagem;
    }

    public void setImagem(Blob imagem) {
        this.imagem = imagem;
    }
    
    
    
}
